    private void cargarDatosTabla(){
        ProductoController pc = new ProductoController();
        DefaultTableModel modelo = (DefaultTableModel) tblListadoProductos.getModel();
        modelo.setRowCount(0);
        for(Producto i : pc.obtenerListaProductos()){
            modelo.addRow(new Object[]{
                i.getId(),
                i.getNombre(),
                i.getPrecio(),
                i.getCantidad()
            });
        }
    }
    
