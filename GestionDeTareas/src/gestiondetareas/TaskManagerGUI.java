    private void btnAgregarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTareaActionPerformed
        
        if(txtNombre.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Debe ingresar un nombre de tarea", "Error de validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        
        boolean estadoTarea = false;
        
        estadoTarea = jrbCompleta.isSelected();
        
        Task tareaNueva = new Task(idTask++, txtNombre.getText(), jclFechaLimite.getDate(), cmbPrioridad.getSelectedIndex(), estadoTarea);
        
        
        p1.agregarTarea(tareaNueva);
        llenarTabla();
        JOptionPane.showMessageDialog(null, "Tarea agregada", "Exito", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnAgregarTareaActionPerformed

    
    private void llenarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) tblTareas.getModel();
        modelo.setRowCount(0);
        
        for(Task i : p1.obtenerTareas()){
            modelo.addRow(new Object[]{
                i.getIdTask(),
                i.getNombre(),
                i.getFechaLimite(),
                i.getPrioridad(),
                i.isCompletada()
            });
        }
    }
    
   
    
    
